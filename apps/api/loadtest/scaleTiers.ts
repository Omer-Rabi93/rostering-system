function printConsoleTable(results: readonly TierResult[]): void {
  console.log('\n\n=== Scale-tier benchmark summary ===\n');
  console.table(
    results.map((r) => ({
      tier: r.tier,
      'import rows/sec': r.csvImport.rowsPerSec.toFixed(1),
      'import ms': r.csvImport.rowProcessingMs.toFixed(0),
      'data-fetch ms': r.rosterGeneration?.dataFetchMs.toFixed(0) ?? 'n/a',
      'problem-build ms': r.rosterGeneration?.problemBuildMs.toFixed(0) ?? 'n/a',
      'solve ms': r.rosterGeneration?.solveMs?.toFixed(0) ?? 'n/a',
      'persist ms': r.rosterGeneration?.persistMs?.toFixed(0) ?? 'n/a',
      'generate total ms': r.rosterGeneration?.totalMs.toFixed(0) ?? 'n/a',
      pass: r.pass === null ? 'n/a' : r.pass ? 'PASS' : 'FAIL',
    })),
  );
  for (const r of results) {
    console.log(`Tier ${r.tier}: ${r.passReason}`);
  }
}

async function writeArtifacts(results: readonly TierResult[]): Promise<void> {
  await mkdir(RESULTS_DIR, { recursive: true });
  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const jsonPath = path.join(RESULTS_DIR, `scale-tiers-${timestamp}.json`);
  const mdPath = path.join(RESULTS_DIR, `scale-tiers-${timestamp}.md`);

  await writeFile(
    jsonPath,
    JSON.stringify(
      { generatedAt: new Date().toISOString(), utilizationRatio: UTILIZATION_RATIO, targetMonth: TARGET_MONTH, results },
      null,
      2,
    ),
    'utf8',
  );

  const mdLines: string[] = [
    `# Scale-tier benchmark — ${new Date().toISOString()}`,
    '',
    `Utilization ratio: ${UTILIZATION_RATIO}. Target month: ${TARGET_MONTH}. Solve reasonable bound: ${SOLVE_REASONABLE_BOUND_MS}ms.`,
    '',
    '| Tier | Import rows/sec | Import ms | Data-fetch ms | Problem-build ms | Solve ms | Persist ms | Generate total ms | Pass |',
    '|---|---|---|---|---|---|---|---|---|',
  ];
  for (const r of results) {
    mdLines.push(
      `| ${r.tier} | ${r.csvImport.rowsPerSec.toFixed(1)} | ${r.csvImport.rowProcessingMs.toFixed(0)} | ` +
        `${r.rosterGeneration?.dataFetchMs.toFixed(0) ?? 'n/a'} | ${r.rosterGeneration?.problemBuildMs.toFixed(0) ?? 'n/a'} | ` +
        `${r.rosterGeneration?.solveMs?.toFixed(0) ?? 'n/a'} | ${r.rosterGeneration?.persistMs?.toFixed(0) ?? 'n/a'} | ` +
        `${r.rosterGeneration?.totalMs.toFixed(0) ?? 'n/a'} | ${r.pass === null ? 'n/a' : r.pass ? 'PASS' : 'FAIL'} |`,
    );
  }
  mdLines.push('', '## Notes', '');
  for (const r of results) {
    mdLines.push(`- Tier ${r.tier}: ${r.passReason}`);
  }
  await writeFile(mdPath, mdLines.join('\n') + '\n', 'utf8');

  console.log(`\nArtifacts written:\n  ${jsonPath}\n  ${mdPath}`);
}

// ---------------------------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------------------------

async function main(): Promise<void> {
  const cliTiers = process.argv.slice(2).map(Number).filter((n) => Number.isFinite(n) && n > 0);
  const tiers = [...(cliTiers.length > 0 ? cliTiers : ALL_TIERS)].sort((a, b) => a - b);

  const solverAvailable = existsSync(VENV_PYTHON);
  if (!solverAvailable) {
    console.warn(
      `\nBLOCKER: ${VENV_PYTHON} not found. Roster-generation stage (data-fetch/problem-build/solve/persist) ` +
        'will be skipped for every tier -- only CSV-import numbers will be real. See solver/README.md to set up ' +
        'the venv (`cd solver && python3 -m venv .venv && source .venv/bin/activate && pip install -r requirements.txt`).\n',
    );
  }

  const databaseUrl = process.env.DATABASE_URL;
  if (!databaseUrl) {
    throw new Error('DATABASE_URL is not set -- copy .env.example to .env at the repo root (see docker-compose.dev.yml).');
  }

  const prisma = createPrismaClient(databaseUrl);
  const boss = createBoss(databaseUrl);

  const results: TierResult[] = [];
  try {
    for (const tier of tiers) {
      const result = await runTier(prisma, boss, tier, solverAvailable);
      results.push(result);
      // Write partial results after every tier -- if a later (larger) tier hangs or the process is
      // killed, the smaller tiers' real numbers are not lost.
      await writeArtifacts(results);
    }
  } finally {
    printConsoleTable(results);
    await prisma.$disconnect();
    await boss.stop({ graceful: false, close: true });
  }
}

main().catch((err: unknown) => {
  console.error('scaleTiers.ts failed:', err);
  process.exitCode = 1;
});

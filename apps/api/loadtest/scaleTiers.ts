// Scale-tier benchmark: 100 / 1,000 / 10,000 workers, CSV import + full-month roster generation,
// staged timing report. See the v4 design doc, Part C, "Scale-tier benchmarks: 100 / 1,000 /
// 10,000 workers, CSV import + full-month generation, staged timing report".
//
// This drives the real service layer DIRECTLY (in-process, real Postgres, real transactions, a
// real spawned Python solver subprocess), never through HTTP/pg-boss -- the design doc is explicit
// that precise internal-stage timing needs to see each internal `await`, which only the direct
// caller can do, and that this benchmark should NOT add permanent timing instrumentation to
// `CsvImportService`/`RosterGenerationService` themselves. Run with:
//
//   cd apps/api && npx tsx loadtest/scaleTiers.ts [tier ...]
//
// e.g. `npx tsx loadtest/scaleTiers.ts 100 1000` to run only a subset (useful for a quick smoke
// test before committing to the full, much slower 10,000-worker tier). Tiers always run smallest
// first regardless of the order given on the command line, per the design doc's "catch a problem
// at a small tier before burning time on the largest" reasoning.
//
// Requires: `docker-compose.dev.yml`'s Postgres up (DATABASE_URL in the repo-root `.env`), and
// `solver/.venv` (see `solver/README.md`) for the roster-generation stage's real CP-SAT solve --
// if the venv is missing, the CSV-import stage still runs and is reported; the roster-generation
// stage is skipped for every tier with a clear console warning instead of silently fabricating
// numbers for a solver that was never actually invoked.


interface TierResult {
  readonly tier: number;
  readonly companyId: number;
  readonly companyName: string;
  readonly csvImport: {
    readonly totalRows: number;
    readonly inserted: number;
    readonly updated: number;
    readonly failed: number;
    readonly rowProcessingMs: number;
    readonly rowsPerSec: number;
  };
  readonly requirementsSetup: { readonly ms: number; readonly roleCounts: Record<Role, number> };
  readonly availabilitySetup: { readonly ms: number; readonly rowsCreated: number };
  readonly rosterGeneration: RosterGenerationTiming | null;
  readonly pass: boolean | null;
  readonly passReason: string;
}

async function runTier(
  prisma: PrismaClient,
  boss: Awaited<ReturnType<typeof createBoss>>,
  tier: number,
  solverAvailable: boolean,
): Promise<TierResult> {
  console.log(`\n=== Tier ${tier} workers ===`);

  const companyName = `LoadTest Scale Tier ${tier} - ${Date.now()}`;
  const company = await prisma.company.create({ data: { name: companyName } });
  console.log(`  created company ${company.id} (${companyName})`);

  const csvImportService = new CsvImportService(prisma, boss);
  const records = buildSyntheticWorkerRecords(tier);
  const csvText = serializeWorkersCsv(records);

  console.log(`  importing ${tier} synthetic workers (in-process CsvImportService.importCsv)...`);
  const importStart = performance.now();
  const importResult = await csvImportService.importCsv(csvText, company.id);
  const importEnd = performance.now();
  const rowProcessingMs = importEnd - importStart;
  const rowsPerSec = importResult.totalRows / (rowProcessingMs / 1000);
  console.log(
    `  import done: ${importResult.inserted} inserted, ${importResult.updated} updated, ` +
      `${importResult.failed} failed in ${rowProcessingMs.toFixed(0)}ms (${rowsPerSec.toFixed(1)} rows/sec)`,
  );
  if (importResult.failed > 0) {
    console.warn(`  WARNING: ${importResult.failed} row(s) failed to import -- first error:`, importResult.errors[0]);
  }

  const reqStart = performance.now();
  const roleCounts = await scaleStaffingRequirements(prisma, company.id);
  const reqEnd = performance.now();
  console.log(`  staffing requirements scaled (utilization ratio ${UTILIZATION_RATIO}): ${JSON.stringify(roleCounts)}`);

  let availabilitySetupMs = 0;
  let availabilityRowsCreated = 0;
  if (solverAvailable) {
    const availStart = performance.now();
    availabilityRowsCreated = await seedAvailability(prisma, company.id, TARGET_MONTH);
    availabilitySetupMs = performance.now() - availStart;
    console.log(
      `  seeded ${availabilityRowsCreated} availability rows for ${TARGET_MONTH} in ${availabilitySetupMs.toFixed(0)}ms`,
    );
  } else {
    console.warn('  solver/.venv not found -- skipping availability seeding and roster-generation stage entirely.');
  }

  console.log('  generating roster (in-process RosterGenerationService.generate, real Python solver subprocess)...');
  const rosterGeneration = await timeRosterGeneration(prisma, company.id, TARGET_MONTH, solverAvailable);
  if (rosterGeneration) {
    if (rosterGeneration.error) {
      console.warn(`  roster generation FAILED: ${rosterGeneration.error}`);
    } else {
      console.log(
        `  roster generation done: data-fetch ${rosterGeneration.dataFetchMs.toFixed(0)}ms, ` +
          `problem-build ${rosterGeneration.problemBuildMs.toFixed(0)}ms, ` +
          `solve ${rosterGeneration.solveMs?.toFixed(0) ?? 'n/a'}ms, ` +
          `persist ${rosterGeneration.persistMs?.toFixed(0) ?? 'n/a'}ms, ` +
          `total ${rosterGeneration.totalMs.toFixed(0)}ms, ` +
          `${rosterGeneration.assignmentsCount ?? 0} assignments, ${rosterGeneration.alertCount ?? 0} alerts`,
      );
    }
  }

  let pass: boolean | null = null;
  let passReason = 'not evaluated';
  if (rosterGeneration) {
    if (rosterGeneration.error) {
      pass = false;
      passReason = rosterGeneration.solveTimedOutOrKilled
        ? `solve timed out / was killed: ${rosterGeneration.error}`
        : `roster generation errored: ${rosterGeneration.error}`;
    } else if (rosterGeneration.solveMs === null) {
      pass = false;
      passReason = 'solve stage never ran (unexpected)';
    } else if (rosterGeneration.solveMs > SOLVE_REASONABLE_BOUND_MS) {
      pass = false;
      passReason = `solve took ${rosterGeneration.solveMs.toFixed(0)}ms, exceeding the ${SOLVE_REASONABLE_BOUND_MS}ms bound`;
    } else {
      pass = true;
      passReason = `solve completed in ${rosterGeneration.solveMs.toFixed(0)}ms, within the ${SOLVE_REASONABLE_BOUND_MS}ms bound`;
    }
  } else {
    passReason = 'solver unavailable (solver/.venv missing) -- roster-generation stage skipped entirely';
  }

  return {
    tier,
    companyId: company.id,
    companyName,
    csvImport: {
      totalRows: importResult.totalRows,
      inserted: importResult.inserted,
      updated: importResult.updated,
      failed: importResult.failed,
      rowProcessingMs,
      rowsPerSec,
    },
    requirementsSetup: { ms: reqEnd - reqStart, roleCounts },
    availabilitySetup: { ms: availabilitySetupMs, rowsCreated: availabilityRowsCreated },
    rosterGeneration,
    pass,
    passReason,
  };
}

